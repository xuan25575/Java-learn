package cn.ccu2.guava.order;


public class FastCode {


    private String value;
    private String meaning;
    private String fastCodeName;

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getMeaning() {
        return meaning;
    }

    public void setMeaning(String meaning) {
        this.meaning = meaning;
    }

    public String getFastCodeName() {
        return fastCodeName;
    }

    public void setFastCodeName(String fastCodeName) {
        this.fastCodeName = fastCodeName;
    }
}
